    <script>
      $(document).ready(function() {
        // Check if there is a stored active menu item
        var activeMenuItem = localStorage.getItem('activeMenuItem');
        if (activeMenuItem) {
            // Remove the 'active' class from all menu items
            $('ul.sidebar-list-main li').removeClass('active');
            // Add the 'active' class to the stored active menu item
            $('ul.sidebar-list-main li').eq(activeMenuItem).addClass('active');
        }

        // jQuery click event handler for menu items
        $('ul.sidebar-list-main li').click(function(event) {
            event.preventDefault(); // Prevent the default behavior of the link
            // Remove the 'active' class from all menu items
            $('ul.sidebar-list-main li').removeClass('active');
            // Add the 'active' class to the clicked menu item
            $(this).addClass('active');
            // Store the index of the clicked menu item in local storage
            var index = $(this).index();
            localStorage.setItem('activeMenuItem', index);
        });
    });
